<script setup lang='ts'>
const randomSlug = () => {
  const chars = 'abcdefghijklmnopqrstuvwxyz0123456789'
  let slug = ''
  for (let i = 0; i < 8; i++) {
    slug += chars.charAt(Math.floor(Math.random() * chars.length))
  }
  return slug
}

definePageMeta({
  redirect: `/${randomSlug()}`
})
</script>